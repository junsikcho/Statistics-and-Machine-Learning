% Q7. 함수 모르면 직접 구해도 됨

clear all
clc
format short

% 데이터 입력
x = [8.7, 9, 11, 8.5, 9.2, 12, 12, 18]; % 마그네슘 양
y = [25, 25, 26, 48, 65, 87, 90, 100];  % 맛 지수

% 데이터 길이
n = length(x); % x의 길이를 계산

% 평균 계산
mean_x = sum(x) / n;
mean_y = sum(y) / n;

% 공분산 계산
gong = sum((x - mean_x) .* (y - mean_y)) / (n - 1); % 요소별 곱 적용

% 표준편차 계산
std_x = sqrt(sum((x - mean_x).^2) / (n - 1));
std_y = sqrt(sum((y - mean_y).^2) / (n - 1));

% 상관 계수 계산
r = gong / (std_x * std_y);

r